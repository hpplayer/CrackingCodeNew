package Chapter8_3_sol2;

public class Song {
	private String name;
	private double length;
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public double getLength() {
		return length;
	}
	public void setLength(double length) {
		this.length = length;
	}
	public String toString(){
		return name;
	}
}

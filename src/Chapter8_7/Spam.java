package Chapter8_7;

public class Spam {
	int id;
	String name;
	
	public Spam(int ID, String name){
		this.id = ID;
		this.name = name;
	}

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}
	
}
